"""Aggregate 175 sweep result JSONs into a markdown summary.

Reads benchmark/results/{preset}/{method}/seed{N}.json and writes
docs/BENCHMARK_RESULTS.md with mean +/- std per (preset, method) for
slope%, cont_delta, baimu_count_delta, baimu_area_delta_ha, wall_seconds.
"""
from __future__ import annotations
import json
import math
from pathlib import Path
from collections import defaultdict

PRESETS = [
    "bishan_clone", "neijiang_clone",
    "plain_small_cons", "plain_large_cons",
    "plain_medium_frag", "mixed_medium_frag", "hilly_small_cons",
]
METHODS = ["Random", "Greedy", "GA", "PPO", "MPC"]
SEEDS = [0, 1, 2, 3, 4]
METRICS = [
    ("slope_pct", "Slope %"),
    ("cont_delta", "Delta Cont"),
    ("baimu_count_delta", "Delta Baimu#"),
    ("baimu_area_delta_ha", "Delta Baimu ha"),
    ("wall_seconds", "Wall (s)"),
]
ROOT = Path(__file__).resolve().parents[1]
RESULTS = ROOT / "benchmark" / "results"
OUT = ROOT / "docs" / "BENCHMARK_RESULTS.md"


def load_run(preset: str, method: str, seed: int) -> dict | None:
    p = RESULTS / preset / method / f"seed{seed}.json"
    if not p.exists():
        return None
    try:
        return json.loads(p.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, UnicodeDecodeError) as e:
        print(f"  ! parse error {p}: {e}")
        return None


def mean_std(xs: list[float]) -> tuple[float, float]:
    if not xs:
        return float("nan"), float("nan")
    n = len(xs)
    mu = sum(xs) / n
    if n == 1:
        return mu, 0.0
    var = sum((x - mu) ** 2 for x in xs) / (n - 1)  # sample std
    return mu, math.sqrt(var)


def fmt(mu: float, sd: float, prec: int = 3) -> str:
    if math.isnan(mu):
        return "—"
    return f"{mu:.{prec}f} ± {sd:.{prec}f}"


def main() -> None:
    rows: dict[tuple[str, str], dict[str, list[float]]] = defaultdict(lambda: defaultdict(list))
    missing: list[tuple[str, str, int]] = []

    for preset in PRESETS:
        for method in METHODS:
            for seed in SEEDS:
                rec = load_run(preset, method, seed)
                if rec is None:
                    missing.append((preset, method, seed))
                    continue
                for k, _ in METRICS:
                    v = rec.get(k)
                    if v is not None:
                        rows[(preset, method)][k].append(float(v))

    n_loaded = sum(len(v[METRICS[0][0]]) for v in rows.values())
    print(f"Loaded {n_loaded}/175 cells. Missing={len(missing)}")
    if missing:
        for m in missing[:10]:
            print(f"  missing {m}")

    OUT.parent.mkdir(parents=True, exist_ok=True)
    lines: list[str] = []
    lines.append("# Synthetic Benchmark Results — Full 175-cell Sweep")
    lines.append("")
    lines.append(
        "5 methods (Random, Greedy, GA, PPO, MPC) × 7 presets × 5 seeds = **175 runs**."
    )
    lines.append(
        "Generated by `scripts/aggregate_results.py`. All values are mean ± "
        "sample standard deviation (n=5)."
    )
    lines.append("")
    lines.append("## Method shorthand")
    lines.append("")
    lines.append("- **Random**: uniform pair sampling under the same exchange budget")
    lines.append("- **Greedy**: connectivity-aware local search, single-step lookahead")
    lines.append("- **GA**: NSGA-II population search (50-pair budget on synth, 500 on real)")
    lines.append("- **PPO**: model-free DRL with parcel-scoring policy")
    lines.append("- **MPC**: contrastive world model + horizon-5 random shooting (this paper)")
    lines.append("")
    lines.append("## Per-preset summary")
    lines.append("")

    for preset in PRESETS:
        lines.append(f"### `{preset}`")
        lines.append("")
        n_blocks = None
        for method in METHODS:
            recs = []
            for seed in SEEDS:
                r = load_run(preset, method, seed)
                if r is not None:
                    recs.append(r)
            if recs and n_blocks is None:
                n_blocks = recs[0].get("n_blocks", "?")
        lines.append(f"`n_blocks={n_blocks}`")
        lines.append("")
        # header
        header = "| Method | Slope % | ΔCont | ΔBaimu# | ΔBaimu ha | Wall (s) |"
        sep = "|--------|---------|-------|---------|-----------|----------|"
        lines.append(header)
        lines.append(sep)
        for method in METHODS:
            stats = rows.get((preset, method), {})
            cells = []
            for key, _ in METRICS:
                vals = stats.get(key, [])
                mu, sd = mean_std(vals)
                if key == "wall_seconds":
                    cells.append(f"{mu:.0f} ± {sd:.0f}" if not math.isnan(mu) else "—")
                elif key == "slope_pct":
                    cells.append(fmt(mu, sd, prec=3))
                elif key == "cont_delta":
                    cells.append(fmt(mu, sd, prec=4))
                elif key == "baimu_count_delta":
                    cells.append(fmt(mu, sd, prec=1))
                elif key == "baimu_area_delta_ha":
                    cells.append(fmt(mu, sd, prec=1))
                else:
                    cells.append(fmt(mu, sd))
            lines.append(f"| {method} | " + " | ".join(cells) + " |")
        lines.append("")

    # Cross-preset method comparison: which method wins on slope per preset
    lines.append("## Slope% leaderboard (lower = better)")
    lines.append("")
    lines.append("| Preset | Best | Slope % | Runner-up | Slope % | MPC vs GA |")
    lines.append("|--------|------|---------|-----------|---------|-----------|")
    for preset in PRESETS:
        slopes = {}
        for method in METHODS:
            vals = rows.get((preset, method), {}).get("slope_pct", [])
            if vals:
                slopes[method] = mean_std(vals)
        if not slopes:
            continue
        ranked = sorted(slopes.items(), key=lambda kv: kv[1][0])  # most negative first
        best, (best_mu, best_sd) = ranked[0]
        ru, (ru_mu, ru_sd) = ranked[1] if len(ranked) > 1 else (None, (float("nan"), 0.0))
        mpc_mu = slopes.get("MPC", (float("nan"), 0))[0]
        ga_mu = slopes.get("GA", (float("nan"), 0))[0]
        if not math.isnan(mpc_mu) and not math.isnan(ga_mu) and ga_mu != 0:
            ratio = mpc_mu / ga_mu
            mpc_vs_ga = f"{ratio:.2f}× (MPC/GA)"
            if mpc_mu < ga_mu:  # MPC more negative = stronger
                pct = (ga_mu - mpc_mu) / abs(ga_mu) * 100
                mpc_vs_ga = f"MPC −{pct:.1f}% deeper"
            else:
                pct = (mpc_mu - ga_mu) / abs(ga_mu) * 100
                mpc_vs_ga = f"GA wins (MPC +{pct:.1f}%)"
        else:
            mpc_vs_ga = "—"
        lines.append(
            f"| `{preset}` | **{best}** | {best_mu:.3f} ± {best_sd:.3f} "
            f"| {ru} | {ru_mu:.3f} ± {ru_sd:.3f} | {mpc_vs_ga} |"
        )
    lines.append("")

    # Compute ratios
    lines.append("## Compute cost (median wall-time per cell, seconds)")
    lines.append("")
    header = "| Preset | " + " | ".join(METHODS) + " |"
    sep = "|--------|" + "|".join(["------"] * len(METHODS)) + "|"
    lines.append(header)
    lines.append(sep)
    for preset in PRESETS:
        cells = []
        for method in METHODS:
            vals = rows.get((preset, method), {}).get("wall_seconds", [])
            if not vals:
                cells.append("—")
                continue
            sv = sorted(vals)
            med = sv[len(sv) // 2] if len(sv) % 2 else (sv[len(sv) // 2 - 1] + sv[len(sv) // 2]) / 2
            cells.append(f"{med:.0f}")
        lines.append(f"| `{preset}` | " + " | ".join(cells) + " |")
    lines.append("")

    lines.append("## Notes on the headline numbers")
    lines.append("")
    lines.append("- `bishan_clone` / `neijiang_clone` are calibrated synthetic clones of the two real counties used in the paper, not new measurements; treat them as reproducibility anchors.")
    lines.append("- MPC was trained with `lambda_rank=5`, 5-model ensemble, horizon=5, top_k=50, 1000 states, 50 actions per step (CPU only).")
    lines.append("- PPO was trained with 100k timesteps per cell. Several plain_large/plain_medium_frag cells hit the 20h Colab session cap and were resumed from checkpoint.")
    lines.append("- Random/Greedy/GA results came from the Plan A generator pass on commit `94e3742`; PPO/MPC came from the Plan B sweep on `09f7855`.")
    lines.append("")
    lines.append(f"## Provenance")
    lines.append("")
    lines.append(f"- 175 JSON inputs under `benchmark/results/{{preset}}/{{method}}/seed{{N}}.json`")
    lines.append(f"- Aggregator: `scripts/aggregate_results.py`")
    lines.append("")

    OUT.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"Wrote {OUT.relative_to(ROOT)}  ({len(lines)} lines)")


if __name__ == "__main__":
    main()
